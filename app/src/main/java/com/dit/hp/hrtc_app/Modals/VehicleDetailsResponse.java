package com.dit.hp.hrtc_app.Modals;

public class VehicleDetailsResponse {
    private String vehicleNo;
    private String vehicleType;
    private String vehicleMakeAndModel;
    private String vehicleIot;
    private String vehicleDetails;
    private String createdBy;
    private String lastUpdatedBy;

    public String getVehicleNo() { return vehicleNo; }
    public String getVehicleType() { return vehicleType; }
    public String getVehicleMakeAndModel() { return vehicleMakeAndModel; }
    public String getVehicleIot() { return vehicleIot; }
    public String getVehicleDetails() { return vehicleDetails; }
    public String getCreatedBy() { return createdBy; }
    public String getLastUpdatedBy() { return lastUpdatedBy; }
}
